"""Sometimes you need to count the total number of elements in a list.The total number of elements in a list is called thr length of the list.
To know the length of a list a frinds list, you can use a special keyword called len. This len keyword is the short form of length."""
friends = ['John', 'Jane', 'Doe']
friends.append('Goat')
